const CMD_GENERATE = 'generate';
const CMD_CHECK = 'check';

const configUrl = new URL('../project-structure.json', import.meta.url);
const markerStart = '<!-- project-structure-gen:start -->';
const markerEnd = '<!-- project-structure-gen:end -->';

